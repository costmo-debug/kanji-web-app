function speechTextToKana(text) {
  let converted = text;
  const replacements = [
    ["絵日記", "えにっき"],
    ["日記", "にっき"],
    ["書いた", "かいた"],
    ["書いて", "かいて"],
    ["書く", "かく"],
    ["走った", "はしった"],
    ["走って", "はしって"],
    ["走る", "はしる"],
    ["校庭", "こうてい"]
  ];
  for (const [from, to] of replacements) {
    converted = converted.replaceAll(from, to);
  }
  return converted;
}

function matches(item, query) {
  const q = normalize(query);
  if (!q) return true;
  return [
    item.k,
    item.on,
    item.kun,
    item.meaning,
    item.example,
    ...item.yomi,
    ...item.words
  ].some((value) => normalize(value).includes(q));
}

